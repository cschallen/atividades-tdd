# 2) Implemente a função soma_dobrada, que recebe como argumento dois números inteiros.
# A função deve imprimir a soma destes números.
# Porém, caso os números sejam iguais, a função deve imprimir o dobro da soma.
# E caso algum dos números seja menor que 1, a função deve imprimir -1. 
#
# Exemplos:
# soma_dobrada(1, 2) → 3
# soma_dobrada(3, 2) → 5
# soma_dobrada(2, 2) → 8
# soma_dobrada(7, 7) → 28
# soma_dobrada(3, -3) → -1
